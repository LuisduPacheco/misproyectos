
package modelo;

public class AutorVO {
    private int idAutor;
    private String nombreAutor;
    private String apellidoAutor;
    private String nacionalidadAutor;
    private String fechaInAutor;
    private String fechaActAutor;

    public AutorVO() {
    }

    public int getIdAutor() {
        return idAutor;
    }

    public void setIdAutor(int idAutor) {
        this.idAutor = idAutor;
    }

    public String getNombreAutor() {
        return nombreAutor;
    }

    public void setNombreAutor(String nombreAutor) {
        this.nombreAutor = nombreAutor;
    }

    public String getApellidoAutor() {
        return apellidoAutor;
    }

    public void setApellidoAutor(String apellidoAutor) {
        this.apellidoAutor = apellidoAutor;
    }

    public String getNacionalidadAutor() {
        return nacionalidadAutor;
    }

    public void setNacionalidadAutor(String nacionalidadAutor) {
        this.nacionalidadAutor = nacionalidadAutor;
    }

    public String getFechaInAutor() {
        return fechaInAutor;
    }

    public void setFechaInAutor(String fechaInAutor) {
        this.fechaInAutor = fechaInAutor;
    }

    public String getFechaActAutor() {
        return fechaActAutor;
    }

    public void setFechaActAutor(String fechaActAutor) {
        this.fechaActAutor = fechaActAutor;
    }
    
    

}

